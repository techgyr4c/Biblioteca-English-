/* ===== CREAR LIBROS ===== */
const booksContainer=document.getElementById("books");

booksData.forEach((book,index)=>{
  booksContainer.innerHTML+=`
  <div class="book" onclick="openBook(${index})">
    <img src="${book.img}">
    <div class="book-title">${book.title}</div>
  </div>
  `;
});

/* ===== ABRIR LIBRO ===== */
function openBook(index){
  document.getElementById("books").style.display="none";
  document.getElementById("reader").style.display="flex";

  const content=document.getElementById("content");
  const title=document.getElementById("bookTitle");

  content.innerHTML="";
  title.innerText=booksData[index].title;

  booksData[index].content.forEach((p,i)=>{
    content.innerHTML+=`
      <p class="${i===0?'first-letter':''}">
        ${wrapWords(p.en)}
      </p>
       <div class="phonetic">
    ${p.ph}
  </div>
      <div class="translation" id="t${i}">
        ${p.es}
      </div>
      <button class="btn-translate" onclick="toggleTranslation(${i})">
        Ver traducción
      </button> 
      <button class="btn-pronounce" onclick="speakParagraph('${p.en.replace(/'/g,"")}')">
  🔊
</button>
<button class="btn-mic" onclick="startListening(${i})">
  🎤
</button>  `
     ;
  });
}

/* ===== VOLVER ===== */
function goBack(){
  document.getElementById("books").style.display="grid";
  document.getElementById("reader").style.display="none";
}

/* ===== TRADUCCION ===== */
function toggleTranslation(i){
  const el=document.getElementById("t"+i);
  el.style.display=el.style.display==="block"?"none":"block";
}

/* ===== PALABRAS CLICKEABLES ===== */
function wrapWords(text){
  return text.split(" ").map(word=>
    `<span onclick="speak('${word.replace(/[^a-zA-Z]/g,'')}')">${word}</span>`
  ).join(" ");
}

/* ===== VOZ ===== */
function speak(word){
  const msg=new SpeechSynthesisUtterance(word);
  msg.lang="en-US";
  speechSynthesis.speak(msg);
}
  
function speakParagraph(text){
  const msg = new SpeechSynthesisUtterance(text);
  msg.lang = "en-US";
  msg.rate = 0.9;
  speechSynthesis.cancel();
  speechSynthesis.speak(msg);
}

function startListening(index){
  const SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;

  if(!SpeechRecognition){
    alert("Tu navegador no soporta reconocimiento de voz");
    return;
  }

  const recognition = new SpeechRecognition();
  recognition.lang = "en-US";
  recognition.interimResults = false;

  recognition.start();

  recognition.onresult = function(event){
    const spoken = event.results[0][0].transcript.toLowerCase();
    const original = booksData
      .flatMap(b => b.content)
      [index].en.toLowerCase();

    console.log("Dijiste:", spoken);

    if(spoken.includes(original.substring(0,10))){
      alert("✅ Bien pronunciado");
    }else{
      alert("❌ Intenta de nuevo");
    }
  };

  recognition.onerror = function(){
    alert("Error al reconocer voz");
  };
}
